    /**
     * 英文别名映射：支持 mode_1 到 mode_23 的查询
     */
    private val ALIASES: Map<String, String> = (1..23).associate { "mode_$it" to "模式$it" }

    /**
     * 查询指定模式和强度等级的基准 timing 数组
     *
     * @param modeName 模式名称（支持 "模式1"、"mode_1" 等）
     * @param level 强度档位（1-5），会自动限制到有效范围
     * @return 对应的 timing 数组，如果模式不存在则返回 null
     */
    fun get(modeName: String, level: Int): LongArray? {
        // 尝试直接查询或通过别名查询
        val key = ALIASES[modeName] ?: modeName
        val levelIndex = (level - 1).coerceIn(0, 4)
        return PATTERNS[key]?.getOrNull(levelIndex)
    }

    /**
     * 检查指定模式是否存在
     */
    fun exists(modeName: String): Boolean {
        val key = ALIASES[modeName] ?: modeName
        return PATTERNS.containsKey(key)
    }

    /**
     * 获取所有支持的模式名称（包括中英文）
     */
    fun getAllModeNames(): List<String> {
        return PATTERNS.keys.toList() + ALIASES.keys.toList()
    }
